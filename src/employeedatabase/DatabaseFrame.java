    public void connectToDatabase(){
        try{
            con = connect.connectToDatabase();
            String sql = "SELECT * FROM empdetail";
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql);
            rs.next();
            txtEmpNo.setText(rs.getString("EmpID"));
            txtName.setText(rs.getString("EmpName"));
            txtAddress.setText(rs.getString("Address"));
            txtSuburb.setText(rs.getString("Suburb"));
            txtPostcode.setText(rs.getString("Postcode"));
            txtPhone.setText(rs.getString("HomePhone"));
            txtMobile.setText(rs.getString("Mobile"));
            txtEmail.setText(rs.getString("Email"));
            
            stmt.close();
            connect.closeConnection(con);
        }
        catch(SQLException sqle){
            msgMessage.showMessageDialog(this,sqle.toString()+"close","Message",javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        }
    public void metaPopulate(){
        try{ 
            con = connect.connectToDatabase();
            stmt = con.createStatement();
            String sql = "SELECT * FROM empdetail";
            rs = stmt.executeQuery(sql);
            ResultSetMetaData rsMetaData = rs.getMetaData();
            DatabaseMetaData dbMetaData = con.getMetaData();
            
            txtDbType.setText(dbMetaData.getDatabaseProductName());
            txtDbVersion.setText(dbMetaData.getDatabaseProductVersion());
            DbDriver.setText(dbMetaData.getDriverName());
            txtTableName.setText(rsMetaData.getTableName(1));
            String space =" ";
            
            
            for(int i =1 ; i<rsMetaData.getColumnCount()+1;i++){
                String left = rsMetaData.getColumnName(i);
                String right = rsMetaData.getColumnTypeName(i)+"\n";
                String line = String.format("%1$-10s %2$10s", left,right);    
                txtTableColumns.setText(txtTableColumns.getText()+line);
            }
            
            connect.closeConnection(con);
        }
        catch(SQLException sqle){
            msgMessage.showMessageDialog(this,sqle.toString(),"Error Message",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        
        
    }

