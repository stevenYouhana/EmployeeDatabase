    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        PrintWriter write;
        try{
            
            write = new PrintWriter(txtFilePath.getText());
            
            for(int col = 0; col<tblDetails.getColumnCount(); col++){
                write.write(tblDetails.getColumnName(col)+" | ");
            }
            write.write("\r\n");
            for(int rec =0; rec<tblDetails.getRowCount(); rec++){
                write.write("\r\n");
            
                for(int col = 0; col<tblDetails.getColumnCount(); col++){
                    write.print(tblDetails.getValueAt(rec, col)+" | ");
                  
                }
            }
            write.close();
            msgMessage.showMessageDialog(this,"File saved successfully","Message",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } 
        
        catch(IOException ioe){
            msgMessage.showMessageDialog(this,ioe.toString(),"Error Message",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReportActionPerformed

